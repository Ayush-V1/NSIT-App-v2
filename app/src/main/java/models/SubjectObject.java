public class SubjectObject {

    public final String value;
    public final String proffh;
    public final String subfh;
    public final String roomfh;
    public final String profsh;
    public final String subsh;
    public final String roomsh;

    // constructor
    public SubjectObject(String value, String proffh, String subfh, String roomfh, String profsh, String subsh, String roomsh) {
        this.value = value;
        this.proffh = proffh;
        this.subfh = subfh;
        this.roomfh = roomfh;
        this.profsh = profsh;
        this.subsh = subsh;
        this.roomsh = roomsh;
    }

}
